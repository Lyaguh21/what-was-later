import { gameInitialState } from "@/entities/game";

export function loadGameState() {
  try {
    return {
      ...gameInitialState,

      topScore: Number(localStorage.getItem("topScore") ?? 0),
      topStreak: Number(localStorage.getItem("topStreak") ?? 0),
      countGame: Number(localStorage.getItem("countGame") ?? 0),
    };
  } catch {
    return gameInitialState;
  }
}
